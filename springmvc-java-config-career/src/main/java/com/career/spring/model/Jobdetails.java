package com.career.spring.model;

public class Jobdetails {
	private String jobid;
	private String jobname;
	private String keyword1;
	private String keyword2;
	private String keyword3;
	private String minexperience;
	private String maxexperience;
	private String city;
	private String country;
	private String jobpostdate;
	private String expirydate;
	public String getJobid() {
		return jobid;
	}
	public void setJobid(String jobid) {
		this.jobid = jobid;
	}
	public String getJobname() {
		return jobname;
	}
	public void setJobname(String jobname) {
		this.jobname = jobname;
	}
	public String getKeyword1() {
		return keyword1;
	}
	public void setKeyword1(String keyword1) {
		this.keyword1 = keyword1;
	}
	public String getKeyword2() {
		return keyword2;
	}
	public void setKeyword2(String keyword2) {
		this.keyword2 = keyword2;
	}
	public String getKeyword3() {
		return keyword3;
	}
	public void setKeyword3(String keyword3) {
		this.keyword3 = keyword3;
	}
	public String getMinexperience() {
		return minexperience;
	}
	public void setMinexperience(String minexperience) {
		this.minexperience = minexperience;
	}
	public String getMaxexperience() {
		return maxexperience;
	}
	public void setMaxexperience(String maxexperience) {
		this.maxexperience = maxexperience;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getJobpostdate() {
		return jobpostdate;
	}
	public void setJobpostdate(String jobpostdate) {
		this.jobpostdate = jobpostdate;
	}
	public String getExpirydate() {
		return expirydate;
	}
	public void setExpirydate(String expirydate) {
		this.expirydate = expirydate;
	}
	

}
