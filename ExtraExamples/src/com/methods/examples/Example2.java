public class Example2 {

	/**
	 * @param args
	 */
	public static int oddOrEven(int a)
	{
		if(a%2==0)
		{
			return 0;
		}
		else return 0;
	}
	
	public static void main(String[] args) {
		int a = oddOrEven(20);
		System.out.println(a);
	}

}
