package com.sampleProgram.Operators;

public class Inc_Dec {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		double number = 5.2;

		
		  System.out.println(number++);
		  System.out.println(number);
		 

        System.out.println(++number);
        System.out.println(number);
	}

}
