package com.sampleProgram.Operators;

public class Arithmetic {

	public static void main(String[] args) {
        double number1 = 12.5;
        double number2 = 3.5;
        double result;
    	       
        result = number1 + number2;
        System.out.println("number1 + number2 = " + result);
    	
        result = number1 - number2;
        System.out.println("number1 - number2 = " + result); 	
    
        result = number1 * number2;
        System.out.println("number1 * number2 = " + result);
      
        result = number1 / number2;
        System.out.println("number1 / number2 = " + result);
    	
        result = number1 % number2;
        System.out.println("number1 % number2 = " + result);
	}

}
