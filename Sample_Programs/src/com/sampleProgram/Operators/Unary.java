package com.sampleProgram.Operators;

public class Unary {

	public static void main(String[] args) {
		
        double number = 5.2;
        boolean flag = false;
    	
        System.out.println("+number = " + +number);
        
        System.out.println("-number = " + -number);
        
        System.out.println("number = " + ++number);
        
        System.out.println("number = " + --number);
        
        System.out.println("!flag = " + !flag);
        
	}

}
